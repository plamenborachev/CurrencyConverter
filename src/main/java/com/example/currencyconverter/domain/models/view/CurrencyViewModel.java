package com.example.currencyconverter.domain.models.view;

public class CurrencyViewModel {

    private String id;
    private String name;

    public CurrencyViewModel() {
    }

    public String getId() {
        return this.id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
